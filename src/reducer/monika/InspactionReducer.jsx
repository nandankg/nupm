import { createSlice, createAsyncThunk } from "@reduxjs/toolkit";
import { InspactionRegisterData } from "../../data/Data";
import { toast } from "react-toastify";
import { showToastOnce } from "../../component/toastUtils";
const user = JSON.parse(localStorage.getItem("userdata"));
console.log(user);
const token = localStorage.getItem("accessToken");
console.log(token);
let deprt = user?.department.toLowerCase();
console.log(deprt);
if (deprt === "afc-mainline") {
  deprt = "afc_mainline";
}
const fType = {
  telecom: "inspection-register-telecom",
  signalling: "inspection-register",
  operation: "daily-transaction-register-Issue",
  afc_mainline: "inspection-register-mainline",
};
export const saveData = createAsyncThunk("data/saveData", async (id) => {
  return fetch(`https://tprosysit.com/upmrc/public/api/${deprt}/edit`, {
    method: "POST",
    headers: {
      Accept: "application/json",
      "Content-Type": "application/json",
      Authorization: `Bearer ${token}`,
    },
    body: JSON.stringify({
      formType: fType[deprt],
      status: "1",
      update_id: id,
    }),
  }).then((res) => res.json());
});

export const fetchData = createAsyncThunk("data/fetchData", async () => {
  return fetch(
    `https://tprosysit.com/upmrc/public/api/register/${deprt}/viewData`,
    {
      method: "POST",
      headers: {
        Accept: "application/json",
        "Content-Type": "application/json",
        Authorization: `Bearer ${token}`,
      },
      body: JSON.stringify({
        formType: fType[deprt],
      }),
    }
  ).then((res) => res.json());
});
export const addData = createAsyncThunk("data/addData", async (values) => {
  console.log(values);
  return fetch(
    `https://tprosysit.com/upmrc/public/api/register/${deprt}/save`,
    {
      method: "POST",
      headers: {
        Accept: "application/json",
        "Content-Type": "application/json",
        Authorization: `Bearer ${token}`,
      },
      body: JSON.stringify({
        formType: fType[deprt],
        station:values.station,
       date:values.date,
        remarks: values.remark,
        observation: values.observation,
        name:values.name,
      sign:values.designation,
        employee_id: values.employee_id || user.profileid,
        department: user.department,
      }),
    }
  ).then((res) => res.json());
});
